package OOPS.Principles.Inheritance;

public class Super
{

    /*
    Whenever a subclass needs to refer to its immidiate superclass,
    it can be done with keyword super
    Super has two general forms:
        1.Calling superclasses constructor
        2.Access a member of superclass that has been hidden by a member of subclass.




    ** Note ** -> Object class is root of class hierarchy. Every class in java is a subclass of Object.
                  So we can use super in any class. It will refer to its superclass(by default Object).
    */

}
